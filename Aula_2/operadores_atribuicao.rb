#Quando queremos atribuir um valor a mais na mesmoa variavel e usado desde jeito: 

puts A = 4 
# Atribui um valor a uma variavel.
puts A = A + 4 
#Equivale A = A + 4
puts A += 4 
#Equivale A = A - 4
puts A -= 4 
#Equivale A = A * 4
puts A *= 4 
#Equivale A = A % 4
puts A %= 4 
#Equivale A = A / 4
puts A /= 4 
#Equivale A = A ** 4
puts A **= 4 

